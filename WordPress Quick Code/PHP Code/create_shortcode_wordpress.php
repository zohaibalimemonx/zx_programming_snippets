    // [zx_get_woo_cart_form id=""]
    function zx_woocommerce_add_to_cart_form($atts)
    {
        $atts = shortcode_atts( array(
            'id' => ''
            ), 
            $atts
        );

        $args = array(
            'post_type'     => array('product'),
            'post_status'   => array('publish'),
            'p'             => $atts['id'],
        );
        $x = new WP_Query($args);

        if( $x->have_posts() )
        {
            $action_result = '';

            while ( $x->have_posts() ) : $x->the_post();

            remove_action('woocommerce_single_product_summary', 'woocommerce_template_single_title', 5);
            remove_action('woocommerce_single_product_summary', 'woocommerce_template_single_rating', 10);
            remove_action('woocommerce_single_product_summary', 'woocommerce_template_single_price', 10);
            // In Woodmart The Priority Of woocommerce_template_single_excerpt is 30 || 31
            remove_action('woocommerce_single_product_summary', 'woocommerce_template_single_excerpt', 31);
            remove_action('woocommerce_single_product_summary', 'woocommerce_template_single_meta', 40);
            remove_action('woocommerce_single_product_summary', 'woocommerce_template_single_sharing', 50);
            
            ob_start();
            do_action( 'woocommerce_single_product_summary' );
            $action_result = ob_get_contents();
            ob_end_clean();

            endwhile;

            $output = $action_result;
            return $output;
        }
        else
        {
            $output = 'No Product Found!';
            return $output;
        }
    }
    add_shortcode( 'zx_get_woo_cart_form', 'zx_woocommerce_add_to_cart_form' );

